    private void btnDetailActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDetailActionPerformed
        // TODO add your handling code here:
        
         int selectRowIndex =  jTable1.getSelectedRow();
       
       if(selectRowIndex < 0){
           JOptionPane.showMessageDialog(this, "Please select a row to show");
           return;
    }
       
        DefaultTableModel model =  (DefaultTableModel)jTable1.getModel();
        Employee selectEmployees = (Employee)model.getValueAt(selectRowIndex, 0);
        
        txtName.setText(selectEmployees.getName().toString());
        txtID.setText(selectEmployees.getEmployeeID());
        txtAge.setText(String.valueOf(selectEmployees.getAge()));
        txtGender.setText(selectEmployees.getGender());
        txtStartDate.setText(selectEmployees.getDate());
        txtLevel.setText(String.valueOf(selectEmployees.getLevel()));
        txtTeam.setText(selectEmployees.getTeamInfo());
        txtPosition.setText(selectEmployees.getPositionTitle());
        txtPhoneNum.setText(selectEmployees.getCellPhoneNum());
        txtEmail.setText(selectEmployees.getEmailAddress());
        ImageIcon icon = new ImageIcon(selectEmployees.getPhoto());
        lblPhotoDisplay.setIcon(icon);

        

    }//GEN-LAST:event_btnDetailActionPerformed

