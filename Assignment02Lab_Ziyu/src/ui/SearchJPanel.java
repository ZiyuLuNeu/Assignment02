    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        
       if(txtName.getText() == "" && txtEmployeeID.getText() == "" && txtCellPhoneNum.getText()== ""){
       JOptionPane.showMessageDialog(this, "please enter your search criteria");
       }
       else {
       String name = txtName.getText();
       String id = txtEmployeeID.getText();
       String cellphoneNum = txtCellPhoneNum.getText();
       
       ArrayList<Employee> employees = history.getHistory();
       List<Employee> filterEmployees = employees;
       if(!name.isEmpty()){
          filterEmployees = filterEmployees.stream().filter(employee->employee.getName().contains(name)).collect(Collectors.toList());
       }
       if(!id.isEmpty()){
          filterEmployees = filterEmployees.stream().filter(employee->employee.getEmployeeID().contains(id)).collect(Collectors.toList());
       }
       if(!cellphoneNum.isEmpty()){
          filterEmployees = filterEmployees.stream().filter(employee->employee.getCellPhoneNum().contains(cellphoneNum)).collect(Collectors.toList());
       }
     
       setHistory(filterEmployees);
       
      JOptionPane.showMessageDialog(this, "information searched");
        
       populateTable();
       }
    }//GEN-LAST:event_jButton1ActionPerformed

